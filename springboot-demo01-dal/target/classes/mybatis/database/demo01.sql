CREATE DATABASE IF NOT EXISTS demo01;

DROP TABLE IF EXISTS `animal`;
CREATE TABLE `animal` (
  `id` BIGINT(16) UNSIGNED AUTO_INCREMENT COMMENT '自增主键id',
  `name` VARCHAR(32) NOT NULL COMMENT '名称',
  `type` VARCHAR(32) NOT NULL DEFAULT '' COMMENT '类型',
  `sex` VARCHAR(32) NOT NULL DEFAULT '' COMMENT '性别',
  `master` VARCHAR(32) DEFAULT NULL COMMENT '主人',
  `created_app` VARCHAR(64) NOT NULL DEFAULT '' COMMENT '创建入口',
  `created_date` DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) COMMENT '创建日期',
  `updated_app` VARCHAR(64) NOT NULL DEFAULT '' COMMENT '更新入口',
  `updated_date` DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3) COMMENT '更新日期',
  PRIMARY KEY (`id`),
  KEY `animal_name` (`name`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='动物?';